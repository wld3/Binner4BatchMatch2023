package edu.umich.wld;

public class FeatureAttributes {

	private Double mass;
	private Double RT;
	private Integer bin;
	
	public Double getMass() {
		return mass;
	}
	
	public void setMass(Double mass) {
		this.mass = mass;
	}
	
	public Double getRT() {
		return RT;
	}
	
	public void setRT(Double RT) {
		this.RT = RT;
	}
	
	public Integer getBin() {
		return bin;
	}
	
	public void setBin(Integer bin) {
		this.bin = bin;
	}
}
